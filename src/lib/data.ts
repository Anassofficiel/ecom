// ─── Helpers ──────────────────────────────────────────────────────────────────
function enrichProduct(product: Product): Product {
  return {
    ...product,
    isPromotion:
      promotionProductIds.includes(product.id) ||
      Boolean(product.discount && product.discount > 0),
  };
}

// ─── Utility functions ────────────────────────────────────────────────────────
export const PRODUCTS_PER_PAGE = 10;

export function getProductsByCategory(category: string): Product[] {
  return products
    .filter((p) => p.category.toLowerCase() === category.toLowerCase())
    .map(enrichProduct);
}

export function getRelatedProducts(productId: string, category: string, count = 6): Product[] {
  return products
    .filter((p) => p.id !== productId && p.category === category)
    .map(enrichProduct)
    .slice(0, count);
}

export function getPaginatedProducts(items: Product[], page: number, perPage = PRODUCTS_PER_PAGE) {
  return items.slice((page - 1) * perPage, page * perPage);
}

export function getTotalPages(items: Product[], perPage = PRODUCTS_PER_PAGE) {
  return Math.ceil(items.length / perPage);
}

export function getPromotionProducts(): Product[] {
  return products
    .filter((p) => promotionProductIds.includes(p.id) || Boolean(p.discount && p.discount > 0))
    .map(enrichProduct);
}

export function getProductById(id: string): Product | undefined {
  const product = products.find((p) => p.id === id);
  return product ? enrichProduct(product) : undefined;
}
export function getAllProducts(): Product[] {
  return products.map(enrichProduct);
}